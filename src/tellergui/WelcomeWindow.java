    private void jButton35ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton35ActionPerformed
                // SUBMIT button on the MONEY ORDER window
        int acc = Integer.parseInt(jTextField20.getText());  // get the account number
        double amount = Double.parseDouble(jTextField21.getText()); // get the amount to deposit
        String name = jTextField19.getText();   // get the name of the person
        Account acMonOrd = bank.findCustomerAccountByNumber(name, acc); // get the actual Account object
        boolean valid = bank.checkName(name); // check the name
        if(valid)
        {
            boolean b = trans.moneyOrder(acMonOrd, amount); // successful transaction returns a true
            if(b)
            {
                transaction.addTransaction(trans); // add transaction to the records
                // print to the GUI window
                printTextFieldMoneyOrder("Money Order for Customer: \n" + name.toUpperCase() +  "\n" + trans.getTransactionGui());
            }            
        }
        else
        {   // print error
            JOptionPane.showMessageDialog(null, "The name you entered is not valid. \nPLEASE TRY AGAIN!!!!");   
        }               
    }//GEN-LAST:event_jButton35ActionPerformed

