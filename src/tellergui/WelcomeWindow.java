    private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton21ActionPerformed

        String name = jTextField6.getText(); // get the name
        String type = jTextField7.getText();// get the Account type
        boolean valid = bank.checkName(name); // check the name
        if(valid) // if the name is valid
        {
            if (type.equalsIgnoreCase("Checking")) // if the account type is checking create a checking account object
            {
                Checking checking = new Checking(); // instance of checking account
                bank.getCustomerToAddAccount(name, checking); // add checking account to customer name
                printTextFieldCreateCustomerAccount(checking.toString());
            } else if (type.equalsIgnoreCase("Savings")) // if the account type is savings create a savings account object
            {
                Savings savings = new Savings(); // instance of savings account
                bank.getCustomerToAddAccount(name, savings); //add savings account to customer name
                printTextFieldCreateCustomerAccount(savings.toString());
            } else if (type.equalsIgnoreCase("Growth")) // if the account type is growth create a growth account object
            {
                Growth growth = new Growth(); // instance of growth account
                bank.getCustomerToAddAccount(name, growth); // add growth account 
                printTextFieldCreateCustomerAccount(growth.toString());
            } 
            else
            {
                JOptionPane.showMessageDialog(null, "You must not have the Account Type correct. \nPLEASE TRY AGAIN!!!!");
            }
        }
        else
        {       // print error
            JOptionPane.showMessageDialog(null, "The name you entered is not valid. \nPLEASE TRY AGAIN!!!!");   
        }     
        
        
        
            
    }//GEN-LAST:event_jButton21ActionPerformed

