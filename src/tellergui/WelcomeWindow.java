    private void jButton33ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton33ActionPerformed
                // SUBMIT button on the TRANSFER window
        String nameFrom = jTextField17.getText();
        String nameTo = jTextField18.getText();
        int accFrom = Integer.parseInt(jTextField14.getText());  // get the account number
        int accTo = Integer.parseInt(jTextField15.getText());  // get the account number
        double amount = Double.parseDouble(jTextField16.getText()); // get the amount to deposit
        Account accoutFrom = bank.findCustomerAccountByNumber(nameFrom, accFrom); // get the actual Account object
        Account accountTo = bank.findCustomerAccountByNumber(nameTo, accTo); // get the actual Account object
        boolean valid1 = bank.checkName(nameFrom); // check the sending name
        boolean valid2 = bank.checkName(nameTo); // check the receiving name
        if(valid1 && valid2) // if 1 and 2 are both valid
        {
                boolean b = trans.transfer(accoutFrom, accountTo, amount); // call on the transfer method
                if(b) // if the transaction returns true
                {
                transaction.addTransaction(trans); // store the record of the transaction itself
                printTextFieldTransfer("Transfer FROM: " + nameFrom.toUpperCase() + "\nTransfer TO: " + nameTo.toUpperCase() + "\n" 
                       + "\n" + trans.getTransactionGui()); // show the transaction that get stored
                }
        }
        else
        {   // print error
            JOptionPane.showMessageDialog(null, "One or both of the names you entered are not valid. \nPLEASE TRY AGAIN!!!!");   
        }     
    }//GEN-LAST:event_jButton33ActionPerformed

